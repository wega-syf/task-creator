<template >

    <form v-on:submit.prevent="onSubmit" class="add-form">
    <!-- Use v-model to do two-way data bind which is gained from the form input -->
    <div class="form-control">
      <label>Task</label>
      <input type="text"
      v-model="text"
      name="text" placeholder="Add Task" />
    </div>

    <div class="form-control">
    <!-- Entities -->
      <label>Day &amp; Time</label>
      <input
        type="text"
        v-model="day"
        name="day"
        placeholder="Add Day &amp; Time"
      />
    </div>

    <div class="form-control form-control-check">
      <label>Set Reminder</label>
      <input type="checkbox"
      v-model="reminder"
      name="reminder" />
    </div>

    <input type="submit" value="Save Task" class="btn btn-block" />
  </form>

</template>
<script>
export default {
    name:'AddTask',
    data() {
        return {
            text:'',
            day:'',
            reminder:false

        }
    },
    methods:{
        onSubmit(){
            // Validate if task is empty
            if (this.text.length < 5){
                alert('Task name must have at least 5 characters')
                return
            }
            const newTask = {
                id : Math.floor(Math.random() * 10000),
                text : this.text,
                day : this.day,
                reminder : this.reminder
            }
            // Emits the new task object to the app 
            this.$emit('add-task',newTask)
            // Clear out the form
            this.text = ''
            this.day = ''
        }

        
    }
}
</script>

